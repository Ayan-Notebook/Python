# Code to take string, int, and float inputs

# String input
string_input = input("Enter a string: ")
print("You entered a string:", string_input)

# Integer input
int_input = int(input("Enter an integer: "))
print("You entered an integer:", int_input)

# Float input
float_input = float(input("Enter a float: "))
print("You entered a float:", float_input)

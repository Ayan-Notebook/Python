import numpy as np

# Array of 10 zeros
array_zeros = np.zeros(10)

# Array of 10 ones
array_ones = np.ones(10)

# Array of 10 fives
array_fives = np.full(10, 5)

# Print the results
print("An array of 10 zeros:")
print(array_zeros)
print("\nAn array of 10 ones:")
print(array_ones)
print("\nAn array of 10 fives:")
print(array_fives)
